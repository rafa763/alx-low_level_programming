
/**
 * op_add - add two numbers
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: sum of the two ints
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: first number to be subtracted
 * @b: second number to be subtracted
 *
 * Return: subtraction of the two ints
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first number to be multiplied
 * @b: second number to be multiplied
 *
 * Return: multiplication of the two ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: divident
 * @b: divisor
 *
 * Return: quotient (int)
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get remainder of division
 * @a: divident
 * @b: divisor
 *
 * Return: remainder (int)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
